local function MatchUp()
    if
        vim.g.loaded_matchup > 0
        and vim.g.matchup_matchparen_offscreen["method"] == "status_manual"
    then
        local status = vim.fn["MatchupStatusOffscreen"]()
        if status ~= nil and string.len(status) > 0 then
            return "Δ " .. status
        end
    end
    return ""
end


